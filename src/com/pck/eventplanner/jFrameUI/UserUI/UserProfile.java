    private void extracomp() {
        evnttbl.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (evnttbl.getSelectedRow() > -1) {
                    int evntAccept = JOptionPane.YES_NO_OPTION;
                    int evntWin = JOptionPane.showConfirmDialog(null, "Do you want to attend this event?", "Attend Event", evntAccept);
                    if (evntWin == 0) {
                        //yes option 
                        JOptionPane.showMessageDialog(null, "Confirmation made!!");

                    } else {
                        //No option 
                        int reqRowIndex = evnttbl.getSelectedRow();
                        String selEvnt = (String) evnttbl.getModel().getValueAt(reqRowIndex, 1);
                        //Searching for selected event to clear the name
                        EventDBMethods eD = new EventDBMethods();
                        UserDBMethods uD = new UserDBMethods();
                        List<Events> evtLst = eD.showAll();
                        List<String> attendees = new ArrayList<>();
                        for (Events ev : evtLst) {
                            if (selEvnt.equalsIgnoreCase(ev.getName())) {
                                for (String str : ev.getAttendees()) {
                                    if (!str.equalsIgnoreCase(usr.getFirst_name())) {
                                        attendees.add(str);
                                    }
                                }
                                ev.setAttendees(attendees);
                                eD.editEvent(ev);
                            }
                        }
                        //Removing from notification
                        List<String> tmp = Arrays.asList(usr.getNotification().split(","));
                        List<String> newNot = new ArrayList<>();
                        for (String s : tmp) {
                            if (!s.equalsIgnoreCase(selEvnt)) {
                                newNot.add(s);
                            }
                        }
                        usr.setNotification(usr.retString(newNot));
                        uD.editProfile(usr);
                    }

                }
            }
        });
    }
