    private void eventUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eventUpdateActionPerformed
        if (!usr.getEvent_planner().equalsIgnoreCase("")) {
            EventDBMethods eD = new EventDBMethods();
            List<Events> evts = eD.showAll();
            Events usrEvt = new Events();
            for (Events temp : evts) {
                if (usr.getEvent_planner().equalsIgnoreCase(temp.getName())) {
                    usrEvt = temp;
                }
            }
            UpdateEvent uE = new UpdateEvent(usrEvt);
            uE.updateEvt();
        } else {
            JOptionPane.showMessageDialog(null, "You haven't created any events!!");
        }
    }//GEN-LAST:event_eventUpdateActionPerformed

