    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        usrnm.setText(usr.getFirst_name());
        EventDBMethods eD = new EventDBMethods();
        UserDBMethods uD = new UserDBMethods();
        List<Events> evtl = eD.showAll();
        DefaultTableModel dm = (DefaultTableModel) evnttbl.getModel();
        int rowCount =  dm.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
        if (usr.getNotification() != null) {
            String[] events = usr.getNotification().split(",");
            for (Events evts : evtl) {
                for (String str : events) {
                    if (str.equalsIgnoreCase(evts.getName())) {
                        String[] temp = evts.getSelDate();
                        dm.addRow(new Object[]{evts.getId(), evts.getName(), evts.getDescription(), temp[0], evts.getLocation()});
                    }
                }
            }
            uD.editProfile(usr);
        }
    }//GEN-LAST:event_formWindowActivated

