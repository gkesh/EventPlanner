    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
         List<String> tmSlt = new ArrayList<>();
        String chk= "Enter you time slots in 24hr format... Eg... 1-2,4-5,12-13,18-19 (No Spaces or seperated with comma)";
        if(!SunFree.getText().equalsIgnoreCase(chk) && SunFree.getText()!=null){
            tmSlt.add("Sunday,"+SunFree.getText());
        }
        if(!MondayFree.getText().equalsIgnoreCase(chk) && MondayFree.getText()!=null){
            tmSlt.add("Monday,"+MondayFree.getText());
        }
        if(!TuesdayFree.getText().equalsIgnoreCase(chk) && TuesdayFree.getText()!=null){
            tmSlt.add("Tuesday,"+TuesdayFree.getText());
        }
        if(!WedFree.getText().equalsIgnoreCase(chk) && WedFree.getText()!=null){
            tmSlt.add("Wednesday,"+WedFree.getText());
        }
        if(!thursFree.getText().equalsIgnoreCase(chk) && thursFree.getText()!=null){
            tmSlt.add("Thursday,"+thursFree.getText());
        }
        if(!friFree.getText().equalsIgnoreCase(chk) && friFree.getText()!=null){
            tmSlt.add("Friday,"+friFree.getText());
        }
        if(!SatFree.getText().equalsIgnoreCase(chk) && SatFree.getText()!=null){
            tmSlt.add("Saturday,"+SatFree.getText());
        }
        int count = tmSlt.size();
        int counter = 0;
        String[][] freeSlt = new String[count][];
        for(String str : tmSlt){
            freeSlt[counter]=str.split(",");
            counter++;
        }
        usr.setFree_slot(freeSlt);
        BusyTSUI bS = new BusyTSUI(usr);
        bS.busy();
        setVisible(false);
        dispose();
    }//GEN-LAST:event_jButton2MouseClicked

